package unit

const (
	// K equals to 1000
	K = 1000
	// Ki equals to 1024
	Ki = 1024

	// M equals to square of K
	M = K * K
	// Mi equals to square of Ki
	Mi = Ki * Ki

	// G equals to M times of K
	G = M * K
	// Gi equals to Mi times of Ki
	Gi = Mi * Ki

	// T equals to G times of K
	T = G * K
	// Ti equals to Gi times of Ki
	Ti = Gi * Ki

	// P equals to T times of K
	P = T * K
	// Pi equals to Ti times of Ki
	Pi = Ti * Ki

	// E equals to P times of K
	E = P * K
	// Ei equals to Pi times of Ki
	Ei = Pi * Ki
)
